package middle_of_linked_list;

/**
 *
 * @author Rsand
 */
public class Solution {
    public ListNode middleNode(ListNode head){
        ListNode slow = head, fast = head;
        
        while(fast != null && fast.next != null){
            slow = slow.next;
            fast = fast.next.next;
        }
        
        return slow;
    }
}
